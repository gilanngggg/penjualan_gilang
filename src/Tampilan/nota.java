    protected void autonumber() {
        try {
            String sql = "SELECT id_nota FROM nota ORDER BY id_nota DESC LIMIT 1";
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery(sql);

            String newKode = "IN0001"; 
            if (rs.next()) {
                String id_nota = rs.getString("id_nota").substring(2); 
                int AN = Integer.parseInt(id_nota) + 1;
                String Nol = "";

                if (AN < 10) {
                    Nol = "000";
                } else if (AN < 100) {
                    Nol = "00";
                } else if (AN < 1000) {
                    Nol = "0";
                } else {
                    Nol = "";
                }

                newKode = "IN" + Nol + AN;
            }

            txtidnota.setText(newKode);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Auto Number Gagal: " + e.getMessage());
        }
    }
    
